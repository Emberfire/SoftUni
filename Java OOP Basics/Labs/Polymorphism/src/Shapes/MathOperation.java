package Shapes;

class MathOperation {
    private int a;
    private int b;
    private int c;
    private int d;

    MathOperation() {
    }

    int add(int a, int b) {
        return a + b;
    }

    int add(int a, int b, int c) {
        return a + b + c;
    }

    int add(int a, int b, int c, int d) {
        return a + b + c + d;
    }
}
